# x,y = y,x

x += 5       # x = x + 5
x -= 5       # x = x - 5
x *= 5       # x = x * 5
x /= 5       # x = x / 5
x %= 5       # x = x % 5
x //= 5       # x = x // 5
x **= 5       # x = x ** 5

values = 1,2,3

x, y, z = values

values1 = 1,2,3,4,5

a,b,*c = values1

print(a,b,c)   # -> 1 2 [3, 4, 5]
print(a,b,c[2])  # -> 1 2 5


